# ==========================================
# 1. 导入必要的库
# ==========================================
# pandas：数据处理库，提供 Series、DataFrame 等数据结构
import pandas as pd
# numpy：数值计算库，提供 nan（缺失值标记）等数学常量
import numpy as np

# ==========================================
# 2. 创建含噪声的湿度数据
# ==========================================
# 用 pd.Series 创建一维数据（类似 Excel 中的一列）
# -100 是传感器故障产生的异常值（噪声），需要清洗掉
# 正常湿度值在 55~57 之间，-100 明显是不合理的
humidity = pd.Series([55.2, 56.1, -100, 55.8, 56.3, -100, 55.7, -100, 56.0])

# 输出原始数据，观察噪声的分布位置（索引 2、5、7）
print("原始数据(含噪声):\n", humidity)

# ==========================================
# 3. 方法 1：后向填充法替换噪声
# ==========================================
# 思路：先用后面的有效值来填补噪声位置
#
# 第一步：humidity.replace(-100, np.nan)
#   将所有 -100 替换为 np.nan（Not a Number）
#   目的：将"异常值"转换为 pandas 标准的"缺失值"标记
#   这样才能使用 pandas 内置的缺失值填充方法
#
# 第二步：.bfill()（Backward Fill，后向填充）
#   用下一个有效的非空值来填充当前缺失值
#   填充方向：从后往前（从下往上）
#   例如：索引 2 的 nan → 用索引 3 的 55.8 填充
#         索引 5 的 nan → 用索引 6 的 55.7 填充
#   注意：如果最后一个值是 nan，则无法填充（没有"下一个"值可用）
cleaned_bfill = humidity.replace(-100, np.nan).bfill()

# 输出后向填充后的结果
print("\n后向填充去噪后:\n", cleaned_bfill)

# ==========================================
# 4. 方法 2：中位数填充法替换噪声
# ==========================================
# 思路：先计算有效数据的中位数，再用该中位数替换所有噪声
# 优点：中位数对极端值鲁棒，不会被异常值拉偏，适合处理含噪声数据

# 第一步：筛选有效数据（排除 -100）
# humidity != -100 生成布尔掩码（True/False 序列）
# humidity[humidity != -100] 用掩码过滤，只保留不等于 -100 的行
valid_data = humidity[humidity != -100]

# 第二步：计算有效数据的中位数
# .median() 返回数值型中位数（加括号调用，得到具体数值而非方法对象）
# 对于 [55.2, 56.1, 55.8, 56.3, 55.7, 56.0] 排序后中位数为 55.9
valid_median = valid_data.median()

# 第三步：用中位数替换所有噪声值（-100 → valid_median）
# .replace(-100, valid_median) 将所有 -100 直接替换为计算好的中位数
cleaned_median = humidity.replace(-100, valid_median)

# 输出中位数填充后的结果
print("\n中位数填充去噪后:\n", cleaned_median)
